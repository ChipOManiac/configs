# Aliases
# -------

# COMMAND CHAINS
alias reset='reset && clear'

# COLORIZE APPLICATIONS ===
alias ls='ls -G'
alias grep='grep --color=auto'
alias fgrep='fgrep --color=auto'
alias egrep='egrep --color=auto'

# SHORT COMMANDS ===
alias ll='ls -l'
alias la='ls -A'
alias l1='ls -1'
alias l='ls -CF'
alias up='cd ..'
alias pauseproc='kill -s SIGSTOP'
alias resproc='kill -s SIGCONT'

alias wmpv="mpv --wid=$WINDOWID"
alias Feh="feh -F"
